async function sha256Text(text) {
  const data = new TextEncoder().encode(text);
  const hash = await crypto.subtle.digest("SHA-256", data);
  return Array.from(new Uint8Array(hash)).map(b => b.toString(16).padStart(2, "0")).join("");
}

async function evaluateClaim(claim) {
  const artifact = claim.artifact || {};
  const path = artifact.path;
  const expected = (artifact.sha256 || "").toLowerCase();
  const content = claim.simulated_artifact_content;
  const checkedAt = new Date().toISOString();

  if (!path) {
    return {
      verdict: "INADMISSIBLE",
      reason: "claim has no artifact path",
      checked_at: checkedAt
    };
  }

  if (content === null || content === undefined) {
    return {
      verdict: "INADMISSIBLE",
      reason: "no execution artifact exists at claimed path",
      checked_at: checkedAt,
      claimed_path: path,
      claimed_sha256: expected || null,
      actual_sha256: null
    };
  }

  const actual = await sha256Text(content);

  if (!expected) {
    return {
      verdict: "INADMISSIBLE",
      reason: "artifact exists but claim lacks expected hash",
      checked_at: checkedAt,
      claimed_path: path,
      claimed_sha256: null,
      actual_sha256: actual
    };
  }

  if (actual !== expected) {
    return {
      verdict: "REJECTED",
      reason: "artifact hash mismatch; evidence is stale or tampered",
      checked_at: checkedAt,
      claimed_path: path,
      claimed_sha256: expected,
      actual_sha256: actual
    };
  }

  return {
    verdict: "ADMISSIBLE",
    reason: "artifact exists and hash matches claim",
    checked_at: checkedAt,
    claimed_path: path,
    claimed_sha256: expected,
    actual_sha256: actual
  };
}

function verdictClass(verdict) {
  return verdict.toLowerCase();
}

function renderResult(result) {
  return `<pre class="result ${verdictClass(result.verdict)}">${JSON.stringify(result, null, 2)}</pre>`;
}

