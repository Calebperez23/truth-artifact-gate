async function loadClaims() {
  try {
    const response = await fetch("sample_claims.json", {cache: "no-store"});
    if (!response.ok) throw new Error("fetch failed");
    return await response.json();
  } catch (_) {
    // Allows the demo to run directly from file:// in browsers that block local fetch.
    return FALLBACK_CLAIMS;
  }
}

async function render() {
  const claims = await loadClaims();
  const container = document.getElementById("cases");
  container.innerHTML = "";

  for (const claim of claims) {
    const result = await evaluateClaim(claim);
    const card = document.createElement("section");
    card.className = "card";
    card.innerHTML = `
      <div class="case-header">
        <h2>${claim.title}</h2>
        <span class="pill ${verdictClass(result.verdict)}">${result.verdict}</span>
      </div>
      <p class="claim">Agent claim: "${claim.agent_claim}"</p>
      <div class="grid">
        <div>
          <h3>Claimed evidence</h3>
          <pre>${JSON.stringify(claim.artifact, null, 2)}</pre>
        </div>
        <div>
          <h3>Gate verdict</h3>
          ${renderResult(result)}
        </div>
      </div>
    `;
    container.appendChild(card);
  }
}

render().catch(err => {
  document.getElementById("cases").innerHTML = `<pre class="result rejected">${err.stack || err}</pre>`;
});


